//! # IEC 61850 Rust Implementation
//! 
//! This library provides a Rust implementation of the IEC 61850 protocol,
//! which is used for communication in electrical substation automation systems.
//! 
//! The implementation is based on the IEC 61850 standard and inspired by the
//! [libiec61850](https://github.com/mz-automation/libiec61850) C library.